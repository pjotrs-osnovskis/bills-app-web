  const langCodes = { lv: 'lv', en: 'en', ru: 'ru' };

  function getLocale() {
    return (global.APP_LOCALE && langCodes[global.APP_LOCALE]) ? global.APP_LOCALE : 'en';
  }

  function t(key) {
    const locale = getLocale();
    const map = APP_STRINGS[locale] || APP_STRINGS.en;
    return map[key] != null ? map[key] : (APP_STRINGS.en[key] != null ? APP_STRINGS.en[key] : key);
  }

  function setLocale(locale) {
    if (langCodes[locale]) global.APP_LOCALE = locale;
  }

  function applyTranslations() {
    const locale = getLocale();
    document.documentElement.lang = locale === 'lv' ? 'lv' : locale === 'ru' ? 'ru' : 'en';
    document.title = t('app.title');
    document.querySelectorAll('[data-i18n]').forEach(function (el) {
      const key = el.getAttribute('data-i18n');
      if (key) el.textContent = t(key);
    });
    document.querySelectorAll('[data-i18n-placeholder]').forEach(function (el) {
      const key = el.getAttribute('data-i18n-placeholder');
      if (key) el.placeholder = t(key);
    });
    document.querySelectorAll('[data-i18n-opt]').forEach(function (el) {
      const key = el.getAttribute('data-i18n-opt');
      if (key) el.textContent = t(key);
    });
    document.querySelectorAll('[data-i18n-title]').forEach(function (el) {
      const key = el.getAttribute('data-i18n-title');
      if (key) el.title = t(key);
    });
    document.querySelectorAll('[data-i18n-aria-label]').forEach(function (el) {
      const key = el.getAttribute('data-i18n-aria-label');
      if (key) el.setAttribute('aria-label', t(key));
    });
  }

